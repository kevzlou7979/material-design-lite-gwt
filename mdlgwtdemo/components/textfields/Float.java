package com.github.ilyes4j.gwt.mdl.components.textfields;

/**
 * In a text field material component, a label can be provided. The label is a
 * text string placed inside the text box when the text box is empty to hint the
 * user about the meaning of the value to be input. This label can be set to
 * Disappear or to move over the text box when the text field acquires the
 * focus. This enumeration provides the possible placement options that can be
 * applied on the label.
 * 
 * @author Mohamed Ilyes DIMASSI
 *
 */
public enum Float {

  /**
   * When the text box acquires the focus, make the label disappear.
   */
  NO_FLOAT,

  /**
   * When the text box acquires the focus, make the label float above the text
   * box.
   */
  FLOAT;
}
