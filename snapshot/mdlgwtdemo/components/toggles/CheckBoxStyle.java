package com.github.ilyes4j.gwt.mdl.components.toggles;

/**
 * Defines the set of possible faces that can be applied on a material
 * Check box.
 * 
 * @author Mohamed Ilyes DIMASSI
 *
 */
public enum CheckBoxStyle {

  /**
   * Conventional check box face.
   */
  CHECKBOX,

  /**
   * Light switch looking face.
   */
  SWITCH,

  /**
   * Icon button looking face.
   */
  ICON
}
